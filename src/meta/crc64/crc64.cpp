uint64_t CRC64_CaseInsensitive(uint64_t crc, const char *buf)
{
    while (*buf)
    {
        char ch = *buf;
        if (ch >= 0x41 && ch <= 0x5A)
            ch |= 0x20;
        crc = crc64_table[((int)(crc >> 56) ^ ch) & 0xFF] ^ (crc << 8);
        buf++;
    }
    return crc;
}

